# data formats
# data: can be any kind of data. I use this mainly in developer functions that remain hidden from the user. For example if id is not 'glottocode'.
# glottodata: User-provided data that contains a glottocode column (data.frame or tibble)
# glottosubdata: User-provided data that contains a glottosubcode column (data.frame or tibble)
# glottologdata: raw data downloaded from glottolog.org (either cldf or csv)
# glottobase: boosted/enriched glottolog data
# glottospace: all glottocodes + geometry column
